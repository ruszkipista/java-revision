class declare_variables {

    public static void main(String[] args) {

        int int_number = 1000;
        float float_number = 10.292f;
        double double_number = 0.00000000000512365123d;
        char char_name = 'N';
        boolean bool_accept = true;
        
        System.out.println("Value of Int is: \n" + int_number);
        System.out.println("Value of Float is: \n" + float_number);
        System.out.println("Value of Double is: \n" + double_number);
        System.out.println("Value of Char is: \n" + char_name);
        System.out.println("Value of Bool is: \n" + bool_accept);
    }
}
